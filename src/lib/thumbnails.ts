/**
 * Returns a dynamic thumbnail URL based on the current time (2-hour intervals).
 * @param courseId The ID of the course.
 * @param type 'hero' or 'card'.
 * @param defaultThumbnail The fallback thumbnail URL.
 */
export function getDynamicThumbnail(
    courseId: string,
    type: 'hero' | 'card',
    defaultThumbnail: string
): string {
    const config = THUMBNAIL_CONFIG[courseId];
    if (!config) return defaultThumbnail;

    const thumbnails = config[type];
    if (!thumbnails || thumbnails.length === 0) return defaultThumbnail;

    // Calculate index based on 2-hour intervals since epoch
    const intervalMs = 2 * 60 * 60 * 1000;
    const index = Math.floor(Date.now() / intervalMs) % thumbnails.length;

    return thumbnails[index];
}
